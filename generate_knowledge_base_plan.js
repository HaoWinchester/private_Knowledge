function run(text, opts = {}) {
  return new TextRun({
    text,
    font: FONT,
    size: opts.size ?? 28,
    bold: opts.bold,
    color: opts.color,
    italics: opts.italics,
  });
}

function p(text, opts = {}) {
  return new Paragraph({
    alignment: opts.alignment ?? AlignmentType.JUSTIFIED,
    heading: opts.heading,
    pageBreakBefore: opts.pageBreakBefore,
    indent: opts.indent,
    spacing: opts.spacing ?? { before: 80, after: 80, line: 420 },
    children: Array.isArray(text) ? text : [run(text, opts.run ?? {})],
  });
}

function h1(text, pageBreakBefore = false) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_1,
    pageBreakBefore,
    spacing: { before: 280, after: 180, line: 420 },
    children: [run(text, { size: 32, bold: true })],
  });
}

function h2(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 220, after: 120, line: 420 },
    children: [run(text, { size: 30, bold: true })],
  });
}

function h3(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_3,
    spacing: { before: 160, after: 80, line: 420 },
    children: [run(text, { size: 28, bold: true })],
  });
}

function bullet(text, level = 0) {
  return new Paragraph({
    numbering: { reference: "bullet-list", level },
    spacing: { before: 40, after: 40, line: 400 },
    children: [run(text)],
  });
}

function numbered(text, level = 0) {
  return new Paragraph({
    numbering: { reference: "number-list", level },
    spacing: { before: 40, after: 40, line: 400 },
    children: [run(text)],
  });
}

function cell(text, width, opts = {}) {
  return new TableCell({
    width: { size: width, type: WidthType.DXA },
    borders,
    shading: opts.header
      ? { fill: "D9EAF7", type: ShadingType.CLEAR }
      : opts.fill
        ? { fill: opts.fill, type: ShadingType.CLEAR }
        : undefined,
    verticalAlign: VerticalAlign.CENTER,
    margins: { top: 100, bottom: 100, left: 120, right: 120 },
    children: [
      new Paragraph({
        alignment: opts.align ?? AlignmentType.CENTER,
        spacing: { before: 0, after: 0, line: 360 },
        children: [run(text, { size: opts.size ?? 24, bold: opts.header })],
      }),
    ],
  });
}

function table(rows, widths) {
  return new Table({
    width: { size: CONTENT_WIDTH, type: WidthType.DXA },
    columnWidths: widths,
    rows: rows.map((row, rowIndex) =>
      new TableRow({
        children: row.map((text, i) =>
          cell(text, widths[i], {
            header: rowIndex === 0,
            align: i === 0 || rowIndex === 0 ? AlignmentType.CENTER : AlignmentType.LEFT,
          }),
        ),
      }),
    ),
  });
}

function blank(lines = 1) {
  return Array.from({ length: lines }, () => p("", { spacing: { before: 100, after: 100 } }));
}

